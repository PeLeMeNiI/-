from random import randint
from_1_to_10  =randint(1,10)
from_10_to_100 = randint(10,100)
from_100_to_1000 = randint(100,1000)

print(f'Random number between 1-10: {from_1_to_10}')
print(f'Random number between 10-100: {from_10_to_100}')
print(f'Random number between 100-1000: {from_100_to_1000}')